public class LacoWhile {
    public static void main(String[] args) {
      // na variavel dependendo do valor ele vai iniciar no valor declarado e vai encerrar enquanto for verdadeira a condiçao
        int  i = 0;
        int j = 10;
        //enquanto for verdadeira ele incrementa
        //while faça em enquanto
        while (i  <= 10){
            System.out.println("i = " + i++);

        }
        while (j >= 0){
            System.out.println("j = " + j--);
        }


    }

}
